def convert_simplified_to_traditional(text):
    """
    將簡體中文轉換為繁體中文
    """
    result = text
    # 按長度排序，先替換長詞
    sorted_items = sorted(SIMPLIFIED_TO_TRADITIONAL.items(),
                     key=lambda x: len(x[0]), reverse=True)
    for simplified, traditional in sorted_items:
        result = result.replace(simplified, traditional)
    return result

